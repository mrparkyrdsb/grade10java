class Average {
    public static void main(String[] args) {
        System.out.println("Average Calculator");

        int mark1 = 52;
        int mark2 = 91;
        int mark3 = 70;
        int mark4 = 82;

        double average = (mark1 + mark2 + mark3 + mark4) / 4;

        System.out.println("Your average is: " + average);
    }
}